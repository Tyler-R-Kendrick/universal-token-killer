describe('installPack / uninstallPack / listInstalledPacks', () => {
  it('installs from a local pack and merges all artifacts', async () => {
    const source = await mkdtemp(path.join(os.tmpdir(), 'utk-install-src-'));
    await writeFixturePack(source);
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-install-ws-'));
    const installed = await installPack(workspace, { type: 'local', path: source }, {
      now: () => new Date('2026-05-19T20:00:00Z')
    });
    expect(installed.name).toBe('git-cli');
    expect(installed.installedAt).toBe('2026-05-19T20:00:00.000Z');

    const config = await readFile(path.join(workspace, '.utk', 'config.toml'), 'utf8');
    expect(config).toContain('utk-pack-begin: git-cli');
    expect(config).toContain('tool = "git"');

    const lark = await readFile(path.join(workspace, '.utk', 'tools', 'git', 'fields', 'ref.lark'), 'utf8');
    expect(lark).toContain('REF');

    // .grammar.json must never be written under .utk/ — lark is the only persisted format.
    const fieldsDir = path.join(workspace, '.utk', 'tools', 'git', 'fields');
    const fieldFiles = await readdir(fieldsDir);
    expect(fieldFiles.every((name) => !name.endsWith('.grammar.json'))).toBe(true);

    const list = await listInstalledPacks(workspace);
    expect(list).toHaveLength(1);
    expect(list[0]?.name).toBe('git-cli');
  });

  it('preserves the previous lockfile when install fails (lockfile-last invariant)', async () => {
    // Install a pack successfully first.
    const source1 = await mkdtemp(path.join(os.tmpdir(), 'utk-atomic-src1-'));
    await writeFixturePack(source1);
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-atomic-ws-'));
    await installPack(workspace, { type: 'local', path: source1 });
    const lockBefore = await import('node:fs/promises').then((fs) => fs.readFile(path.join(workspace, '.utk', 'packs.lock.toml'), 'utf8'));
    expect(lockBefore).toContain('git-cli');

    // Stage a second install that will fail mid-way (config.toml turned into a directory).
    const source2 = await mkdtemp(path.join(os.tmpdir(), 'utk-atomic-src2-'));
    await writeFixturePack(source2, {
      manifest: '[pack]\nname = "other-pack"\nversion = "1.0.0"\ndescription = "x"\nlicense = "MIT"\n[compatibility]\nutk = "^0.1"\n'
    });
    await import('node:fs/promises').then(async (fs) => {
      await fs.rm(path.join(workspace, '.utk', 'config.toml'), { force: true });
      await fs.mkdir(path.join(workspace, '.utk', 'config.toml'), { recursive: true });
    });
    await expect(installPack(workspace, { type: 'local', path: source2 }, { skipLint: true })).rejects.toThrow();

    // The lockfile must still report the original pack only — no torn write, no half-applied state.
    const lockAfter = await import('node:fs/promises').then((fs) => fs.readFile(path.join(workspace, '.utk', 'packs.lock.toml'), 'utf8'));
    expect(lockAfter).toBe(lockBefore);
    expect(lockAfter).not.toContain('other-pack');
  });

  it('rejects remote tarball URLs from the built-in fetcher', async () => {
    const { fetchPackToTempDir } = await import('../src/pack/fetcher.js');
    await expect(fetchPackToTempDir({ type: 'tarball', path: 'https://example.com/pack.tgz' }, os.tmpdir())).rejects.toThrow(/Remote tarball URLs are not supported/);
  });

  it('rolls back grammars with no seed (covers undefined-observations branch)', async () => {
    const source = await mkdtemp(path.join(os.tmpdir(), 'utk-install-noseed-src-'));
    await writeFixturePack(source, {
      // grammar declared but no .grammar.json seed file
      grammars: { 'grammars/git/ref.lark': 'start: VALUE\nVALUE: /[A-Za-z]+/\n' }
    });
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-install-noseed-ws-'));
    await import('node:fs/promises').then((fs) => fs.mkdir(path.join(workspace, '.utk', 'config.toml'), { recursive: true }));
    await expect(installPack(workspace, { type: 'local', path: source })).rejects.toThrow();
    const exists = await import('node:fs/promises').then(async (fs) => {
      try { await fs.stat(path.join(workspace, '.utk', 'packs', 'git-cli')); return true; } catch { return false; }
    });
    expect(exists).toBe(false);
  });

  it('rolls back even when an individual rollback step fails', async () => {
    // Pre-create cache file as a directory so the templates-cache cleanup step throws.
    // The other rollback steps must still run so .utk/packs/<name> is removed.
    const source = await mkdtemp(path.join(os.tmpdir(), 'utk-install-rollback-partial-src-'));
    await writeFixturePack(source);
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-install-rollback-partial-ws-'));
    const { mkdir } = await import('node:fs/promises');
    await mkdir(path.join(workspace, '.utk', 'cache', 'templates', 'git-cli.json'), { recursive: true });
    await mkdir(path.join(workspace, '.utk', 'config.toml'), { recursive: true });
    await expect(installPack(workspace, { type: 'local', path: source })).rejects.toThrow();
    const exists = await import('node:fs/promises').then(async (fs) => {
      try { await fs.stat(path.join(workspace, '.utk', 'packs', 'git-cli')); return true; } catch { return false; }
    });
    expect(exists).toBe(false);
  });

  it('rolls back partial install state when a downstream step fails', async () => {
    const source = await mkdtemp(path.join(os.tmpdir(), 'utk-install-rollback-src-'));
    await writeFixturePack(source);
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-install-rollback-ws-'));
    // Pre-create config.toml as a directory so addPackRegistryBlocks' writeFile throws.
    await import('node:fs/promises').then((fs) => fs.mkdir(path.join(workspace, '.utk', 'config.toml'), { recursive: true }));
    await expect(installPack(workspace, { type: 'local', path: source })).rejects.toThrow();
    // Pack directory and template cache must have been rolled back.
    const exists = await import('node:fs/promises').then(async (fs) => {
      try { await fs.stat(path.join(workspace, '.utk', 'packs', 'git-cli')); return true; } catch { return false; }
    });
    expect(exists).toBe(false);
  });

  it('refuses to install a pack whose name contains path-traversal segments', async () => {
    const source = await mkdtemp(path.join(os.tmpdir(), 'utk-install-traversal-src-'));
    await writeFixturePack(source, {
      manifest: '[pack]\nname = "../escapee"\nversion = "1.0.0"\n'
    });
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-install-traversal-ws-'));
    await expect(installPack(workspace, { type: 'local', path: source }, { skipLint: true })).rejects.toThrow(/Path traversal/);
  });

  it('reinstalls with force and removes prior artifacts', async () => {
    const source = await mkdtemp(path.join(os.tmpdir(), 'utk-install-force-src-'));
    await writeFixturePack(source);
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-install-force-ws-'));
    await installPack(workspace, { type: 'local', path: source });
    await expect(installPack(workspace, { type: 'local', path: source })).rejects.toThrow(/already installed/);
    const reinstalled = await installPack(workspace, { type: 'local', path: source }, { force: true });
    expect(reinstalled.name).toBe('git-cli');
    const config = await readFile(path.join(workspace, '.utk', 'config.toml'), 'utf8');
    expect(config.match(/utk-pack-begin: git-cli/g)).toHaveLength(1);
  });

  it('uninstalls and reverses all on-disk effects', async () => {
    const source = await mkdtemp(path.join(os.tmpdir(), 'utk-uninstall-src-'));
    await writeFixturePack(source);
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-uninstall-ws-'));
    await installPack(workspace, { type: 'local', path: source });
    await uninstallPack(workspace, 'git-cli');
    const config = await readFile(path.join(workspace, '.utk', 'config.toml'), 'utf8');
    expect(config).not.toContain('utk-pack-begin');
    const list = await listInstalledPacks(workspace);
    expect(list).toEqual([]);
    const remaining = await readdir(path.join(workspace, '.utk', 'packs')).catch(() => [] as string[]);
    expect(remaining).not.toContain('git-cli');
  });

  it('throws when uninstalling an unknown pack', async () => {
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-uninstall-missing-'));
    await expect(uninstallPack(workspace, 'absent')).rejects.toThrow(/not installed/);
  });

  it('removes the .lark artifact during uninstall and writes no .grammar.json on install', async () => {
    const source = await mkdtemp(path.join(os.tmpdir(), 'utk-uninstall-lark-src-'));
    await writeFixturePack(source);
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-uninstall-lark-ws-'));
    await installPack(workspace, { type: 'local', path: source });
    // Walk the workspace and assert no .grammar.json files exist.
    async function findGrammarJson(dir: string): Promise<string[]> {
      const out: string[] = [];
      const stack = [dir];
      while (stack.length > 0) {
        const current = stack.pop()!;
        const entries = await readdir(current, { withFileTypes: true });
        for (const entry of entries) {
          const full = path.join(current, entry.name);
          if (entry.isDirectory()) stack.push(full);
          else if (entry.name.endsWith('.grammar.json')) out.push(full);
        }
      }
      return out;
    }
    expect(await findGrammarJson(path.join(workspace, '.utk'))).toEqual([]);
    await uninstallPack(workspace, 'git-cli');
    await expect(stat(path.join(workspace, '.utk', 'tools', 'git', 'fields', 'ref.lark'))).rejects.toThrow();
    const list = await listInstalledPacks(workspace);
    expect(list).toEqual([]);
  });

  it('removes lark artifact even when seed observations are zero', async () => {
    const source = await mkdtemp(path.join(os.tmpdir(), 'utk-uninstall-nogrammar-src-'));
    await writeFixturePack(source, {
      manifest: [
        '[pack]',
        'name = "no-seed"',
        'version = "1.0.0"',
        '',
        '[[grammars]]',
        'tool = "git"',
        'field = "ref"',
        ''
      ].join('\n'),
      tools: {},
      grammars: { 'grammars/git/ref.lark': defaultLark() },
      templates: {}
    });
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-uninstall-nogrammar-ws-'));
    await installPack(workspace, { type: 'local', path: source });
    await uninstallPack(workspace, 'no-seed');
    await expect(stat(path.join(workspace, '.utk', 'tools', 'git', 'fields', 'ref.lark'))).rejects.toThrow();
  });

  it('caches template descriptors when templates are present', async () => {
    const source = await mkdtemp(path.join(os.tmpdir(), 'utk-install-tpl-src-'));
    await writeFixturePack(source);
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-install-tpl-ws-'));
    await installPack(workspace, { type: 'local', path: source });
    const cachePath = path.join(workspace, '.utk', 'cache', 'templates', 'git-cli.json');
    const cached = JSON.parse(await readFile(cachePath, 'utf8')) as { templates: Array<{ id: string }> };
    expect(cached.templates[0]?.id).toBe('git.checkout');
  });

  it('skips lint when skipLint is set', async () => {
    const source = await mkdtemp(path.join(os.tmpdir(), 'utk-install-skiplint-src-'));
    await mkdir(source, { recursive: true });
    await mkdir(path.join(source, 'tools'), { recursive: true });
    await writeFile(
      path.join(source, 'utk.pack.toml'),
      [
        '[pack]',
        'name = "broken"',
        'version = "1.0.0"',
        '',
        '[[tools]]',
        'id = "git"',
        'kind = "bash-like"',
        ''
      ].join('\n'),
      'utf8'
    );
    await writeFile(path.join(source, 'tools', 'git.toml'), '[tool]\nid = "git"\n', 'utf8');
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-install-skiplint-ws-'));
    await expect(installPack(workspace, { type: 'local', path: source })).rejects.toThrow(/lint/i);
    const installed = await installPack(workspace, { type: 'local', path: source }, { skipLint: true });
    expect(installed.name).toBe('broken');
  });

  it('accepts an injected fetcher to install from non-local sources', async () => {
    const stage = await mkdtemp(path.join(os.tmpdir(), 'utk-fake-fetcher-src-'));
    await writeFixturePack(stage);
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-fake-fetcher-ws-'));
    const fakeFetcher: PackFetcher = async () => ({ dir: stage, revision: 'sha:deadbeef' });
    const installed = await installPack(workspace, { type: 'git', url: 'https://example.com/git-cli' }, { fetcher: fakeFetcher });
    expect(installed.revision).toBe('sha:deadbeef');
    expect(installed.source).toContain('git:https://example.com/git-cli');
  });
});

