function defaultLark(): string {
  return [
    '# generated by author',
    'start: REF',
    'REF: /[A-Za-z0-9_\\-\\/]{6,64}/',
    ''
  ].join('\n');
}

function defaultSeedGrammar(): FieldGrammar {
  const merged = mergeFieldGrammar(inferFieldGrammar('feature/login-fix'), inferFieldGrammar('main'));
  return mergeFieldGrammar(merged, inferFieldGrammar('release-v1.0'));
}

function defaultTemplate(): string {
  return [
    "import { defineTemplate, grammarSlot } from '@utk/core';",
    '',
    'export default defineTemplate({',
    "  id: 'git.checkout',",
    "  tool: 'git',",
    "  prompt: 'git checkout {{ref}}',",
    '  slots: {',
    "    ref: grammarSlot({ tool: 'git', field: 'ref' })",
    '  }',
    '});',
    ''
  ].join('\n');
}

describe('compileLark', () => {
  it('emits deterministic Lark with observed separators', () => {
    const grammar = mergeFieldGrammar(inferFieldGrammar('feature/login-fix'), inferFieldGrammar('main'));
    const a = compileLark(grammar);
    const b = compileLark(grammar);
    expect(a).toBe(b);
    expect(a).toContain('start: VALUE');
    expect(a).toContain('VALUE: /[A-Za-z0-9');
    expect(a).toContain('observations:');
  });

  it('accepts a custom rule name and exact length range', () => {
    const grammar: FieldGrammar = {
      version: 1,
      observations: 1,
      separators: { '/': { tight: 1, loose: 0 }, '_': { tight: 0, loose: 1 }, '?': { tight: 0, loose: 0 } },
      lengthRange: { min: 5, max: 5 }
    };
    const output = compileLark(grammar, { ruleName: 'REF' });
    expect(output).toContain('start: REF');
    expect(output).toMatch(/REF: \/\[A-Za-z0-9[^\]]*\]\{5\}\//);
  });

  it('preserves alphanumeric separators when escaping', () => {
    const grammar: FieldGrammar = {
      version: 1,
      observations: 1,
      separators: { a: { tight: 1, loose: 0 } },
      lengthRange: { min: 1, max: 3 }
    };
    const output = compileLark(grammar);
    expect(output).toContain('VALUE: /[A-Za-z0-9a]{1,3}/');
  });

  it('clamps length to at least 1', () => {
    const grammar: FieldGrammar = {
      version: 1,
      observations: 1,
      separators: {},
      lengthRange: { min: 0, max: 0 }
    };
    const output = compileLark(grammar);
    expect(output).toContain('VALUE: /[A-Za-z0-9]{1}/');
  });
});

describe('subtractFieldGrammar', () => {
  it('round-trips through merge', () => {
    const seed = mergeFieldGrammar(inferFieldGrammar('feature/login-fix'), inferFieldGrammar('main'));
    const observation = inferFieldGrammar('release-v1.0');
    const merged = mergeFieldGrammar(seed, observation);
    const restored = subtractFieldGrammar(merged, observation);
    expect(restored).toBeDefined();
    expect(restored?.observations).toBe(seed.observations);
  });

  it('returns undefined when subtraction fully consumes observations', () => {
    const seed = inferFieldGrammar('main');
    const result = subtractFieldGrammar(seed, seed);
    expect(result).toBeUndefined();
  });

  it('drops separator entries that hit zero and clamps negative remainders', () => {
    const current: FieldGrammar = {
      version: 1,
      observations: 5,
      separators: { '-': { tight: 3, loose: 0 }, '/': { tight: 2, loose: 0 } },
      lengthRange: { min: 1, max: 10 }
    };
    const seed: FieldGrammar = {
      version: 1,
      observations: 2,
      separators: { '-': { tight: 3, loose: 0 }, '/': { tight: 10, loose: 0 } },
      lengthRange: { min: 1, max: 10 }
    };
    const reduced = subtractFieldGrammar(current, seed);
    expect(reduced?.separators['-']).toBeUndefined();
    expect(reduced?.separators['/']).toBeUndefined();
  });
});

describe('parsePackSource', () => {
  it('parses local paths', () => {
    expect(parsePackSource('./my-pack')).toEqual({ type: 'local', path: './my-pack' });
    expect(parsePackSource('/abs/path')).toEqual({ type: 'local', path: '/abs/path' });
    expect(parsePackSource('../sibling')).toEqual({ type: 'local', path: '../sibling' });
  });

  it('parses tarball paths and URLs', () => {
    expect(parsePackSource('./pack.tgz')).toEqual({ type: 'tarball', path: './pack.tgz' });
    expect(parsePackSource('https://example.com/pack.tar.gz')).toEqual({ type: 'tarball', path: 'https://example.com/pack.tar.gz' });
    expect(parsePackSource('pack.tgz')).toEqual({ type: 'tarball', path: 'pack.tgz' });
  });

  it('parses git short specs and full URLs', () => {
    expect(parsePackSource('github:alice/utk-pack-git')).toEqual({ type: 'git', url: 'https://github.com/alice/utk-pack-git' });
    expect(parsePackSource('github:alice/utk-pack-git#v1')).toEqual({ type: 'git', url: 'https://github.com/alice/utk-pack-git', ref: 'v1' });
    expect(parsePackSource('gitlab:alice/repo')).toEqual({ type: 'git', url: 'https://gitlab.com/alice/repo' });
    expect(parsePackSource('git+https://example.com/repo.git')).toEqual({ type: 'git', url: 'https://example.com/repo.git' });
    expect(parsePackSource('git+https://example.com/repo.git#main')).toEqual({ type: 'git', url: 'https://example.com/repo.git', ref: 'main' });
    expect(parsePackSource('git@example.com:repo.git')).toEqual({ type: 'git', url: 'git@example.com:repo.git' });
    expect(parsePackSource('https://example.com/repo')).toEqual({ type: 'git', url: 'https://example.com/repo' });
  });

  it('parses npm specs including scoped names', () => {
    expect(parsePackSource('utk-git-cli')).toEqual({ type: 'npm', spec: 'utk-git-cli' });
    expect(parsePackSource('@utk/git-cli')).toEqual({ type: 'npm', spec: '@utk/git-cli' });
    expect(parsePackSource('utk-git-cli@^1.0.0')).toEqual({ type: 'npm', spec: 'utk-git-cli@^1.0.0' });
  });

  it('rejects unrecognized and empty sources', () => {
    expect(() => parsePackSource('')).toThrow();
    expect(() => parsePackSource('   ')).toThrow();
    expect(() => parsePackSource('!!invalid')).toThrow();
    expect(() => parsePackSource('github:')).toThrow();
  });

  it('describes sources for logging', () => {
    expect(describePackSource({ type: 'local', path: './x' })).toBe('local:./x');
    expect(describePackSource({ type: 'tarball', path: 'p.tgz' })).toBe('tarball:p.tgz');
    expect(describePackSource({ type: 'git', url: 'https://example.com/r', ref: 'main' })).toBe('git:https://example.com/r#main');
    expect(describePackSource({ type: 'git', url: 'https://example.com/r' })).toBe('git:https://example.com/r');
    expect(describePackSource({ type: 'npm', spec: '@utk/x' })).toBe('npm:@utk/x');
  });

  it('rejects non-string inputs', () => {
    expect(() => parsePackSource(undefined as unknown as string)).toThrow();
  });
});

