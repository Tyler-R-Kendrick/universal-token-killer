async function writeFixturePack(dir: string, overrides: { manifest?: string; tools?: Record<string, string>; grammars?: Record<string, string>; templates?: Record<string, string> } = {}): Promise<void> {
  const manifest = overrides.manifest ?? defaultManifest();
  await mkdir(dir, { recursive: true });
  await writeFile(path.join(dir, 'utk.pack.toml'), manifest, 'utf8');
  const tools = overrides.tools ?? { 'tools/git.toml': defaultToolToml() };
  for (const [relative, content] of Object.entries(tools)) {
    const target = path.join(dir, relative);
    await mkdir(path.dirname(target), { recursive: true });
    await writeFile(target, content, 'utf8');
  }
  const grammars = overrides.grammars ?? { 'grammars/git/ref.lark': defaultLark() };
  for (const [relative, content] of Object.entries(grammars)) {
    const target = path.join(dir, relative);
    await mkdir(path.dirname(target), { recursive: true });
    await writeFile(target, content, 'utf8');
  }
  const templates = overrides.templates ?? { 'templates/git-checkout.template.ts': defaultTemplate() };
  for (const [relative, content] of Object.entries(templates)) {
    const target = path.join(dir, relative);
    await mkdir(path.dirname(target), { recursive: true });
    await writeFile(target, content, 'utf8');
  }
}

