describe('registryRewrite', () => {
  it('renders, adds, and removes pack blocks idempotently', async () => {
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-registry-'));
    const pack: LoadedPack = makeLoadedPack();
    await addPackRegistryBlocks(workspace, 'git-cli', pack.tools);
    let text = await readFile(path.join(workspace, '.utk', 'config.toml'), 'utf8');
    expect(text).toContain(`${PACK_BEGIN_PREFIX} git-cli`);
    expect(text).toContain(`${PACK_END_PREFIX} git-cli`);
    expect(text).toContain('[[tools.registry]]');
    expect(text).toContain('[[tools.registry.structured_fields]]');
    await addPackRegistryBlocks(workspace, 'git-cli', pack.tools);
    text = await readFile(path.join(workspace, '.utk', 'config.toml'), 'utf8');
    expect(text.match(/utk-pack-begin: git-cli/g)).toHaveLength(1);
    await removePackRegistryBlocks(workspace, 'git-cli');
    text = await readFile(path.join(workspace, '.utk', 'config.toml'), 'utf8');
    expect(text).not.toContain('utk-pack');
  });

  it('rendering omits structured fields when none and falls back when tool source has no .tool table', () => {
    const block = renderPackRegistryBlock('p', {
      entry: { id: 'foo', kind: 'bash-like' },
      source: { description: 'flat', parameters: 'nope' as unknown as unknown[] }
    });
    expect(block).toContain('tool = "foo"');
    expect(block).toContain('description = "flat"');
    expect(block).not.toContain('structured_fields');
  });

  it('strips required and description fields when omitted', () => {
    const block = renderPackRegistryBlock('p', {
      entry: { id: 'foo', kind: 'structured' },
      source: { tool: {}, parameters: [{ name: 'a', completions: ['x'] }, { invalid: true }] }
    });
    expect(block).toContain('completions = ["x"]');
  });

  it('skips non-object and unnamed parameter entries', () => {
    const block = renderPackRegistryBlock('p', {
      entry: { id: 'foo', kind: 'structured' },
      source: {
        tool: {},
        parameters: [null, 'oops', { name: 'real', completions: 'not-array' as unknown as string[], required: true, description: 'rd' }]
      }
    });
    expect(block).toContain('name = "real"');
    expect(block).toContain('completions = []');
    expect(block).toContain('required = true');
    expect(block).toContain('description = "rd"');
  });

  it('handles trailing newline in pre-existing config without duplicating', async () => {
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-registry-nl-'));
    await mkdir(path.join(workspace, '.utk'), { recursive: true });
    await writeFile(path.join(workspace, '.utk', 'config.toml'), '', 'utf8');
    const pack = makeLoadedPack();
    await addPackRegistryBlocks(workspace, 'git-cli', pack.tools);
    const text = await readFile(path.join(workspace, '.utk', 'config.toml'), 'utf8');
    expect(text).toContain('utk-pack-begin: git-cli');
  });

  it('adds a trailing newline when the existing config lacks one', async () => {
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-registry-no-nl-'));
    await mkdir(path.join(workspace, '.utk'), { recursive: true });
    await writeFile(path.join(workspace, '.utk', 'config.toml'), '[serialization]\ndefault = "toon"', 'utf8');
    const pack = makeLoadedPack();
    await addPackRegistryBlocks(workspace, 'git-cli', pack.tools);
    const text = await readFile(path.join(workspace, '.utk', 'config.toml'), 'utf8');
    expect(text).toContain('[serialization]\ndefault = "toon"\n');
    expect(text).toContain('utk-pack-begin: git-cli');
  });

  it('removeBlocksForPack tolerates missing markers', () => {
    expect(removeBlocksForPack('[serialization]\ndefault = "toon"\n', 'absent')).toContain('[serialization]');
  });
});

