describe('installPack / uninstallPack / listInstalledPacks', () => {
  it('installs from a local pack and merges all artifacts', async () => {
    const source = await mkdtemp(path.join(os.tmpdir(), 'utk-install-src-'));
    await writeFixturePack(source);
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-install-ws-'));
    const installed = await installPack(workspace, { type: 'local', path: source }, {
      now: () => new Date('2026-05-19T20:00:00Z')
    });
    expect(installed.name).toBe('git-cli');
    expect(installed.installedAt).toBe('2026-05-19T20:00:00.000Z');

    const config = await readFile(path.join(workspace, '.utk', 'config.toml'), 'utf8');
    expect(config).toContain('utk-pack-begin: git-cli');
    expect(config).toContain('tool = "git"');

    const lark = await readFile(path.join(workspace, '.utk', 'tools', 'git', 'fields', 'ref.lark'), 'utf8');
    expect(lark).toContain('REF');

    const grammar = await loadFieldGrammar(workspace, 'git', 'ref');
    expect(grammar?.observations).toBeGreaterThan(0);

    const list = await listInstalledPacks(workspace);
    expect(list).toHaveLength(1);
    expect(list[0]?.name).toBe('git-cli');
  });

  it('reinstalls with force and removes prior artifacts', async () => {
    const source = await mkdtemp(path.join(os.tmpdir(), 'utk-install-force-src-'));
    await writeFixturePack(source);
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-install-force-ws-'));
    await installPack(workspace, { type: 'local', path: source });
    await expect(installPack(workspace, { type: 'local', path: source })).rejects.toThrow(/already installed/);
    const reinstalled = await installPack(workspace, { type: 'local', path: source }, { force: true });
    expect(reinstalled.name).toBe('git-cli');
    const config = await readFile(path.join(workspace, '.utk', 'config.toml'), 'utf8');
    expect(config.match(/utk-pack-begin: git-cli/g)).toHaveLength(1);
  });

  it('uninstalls and reverses all on-disk effects', async () => {
    const source = await mkdtemp(path.join(os.tmpdir(), 'utk-uninstall-src-'));
    await writeFixturePack(source);
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-uninstall-ws-'));
    await installPack(workspace, { type: 'local', path: source });
    await uninstallPack(workspace, 'git-cli');
    const config = await readFile(path.join(workspace, '.utk', 'config.toml'), 'utf8');
    expect(config).not.toContain('utk-pack-begin');
    const list = await listInstalledPacks(workspace);
    expect(list).toEqual([]);
    const remaining = await readdir(path.join(workspace, '.utk', 'packs')).catch(() => [] as string[]);
    expect(remaining).not.toContain('git-cli');
  });

  it('throws when uninstalling an unknown pack', async () => {
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-uninstall-missing-'));
    await expect(uninstallPack(workspace, 'absent')).rejects.toThrow(/not installed/);
  });

  it('subtracts seed observations during uninstall and preserves residual learning', async () => {
    const source = await mkdtemp(path.join(os.tmpdir(), 'utk-uninstall-residual-src-'));
    await writeFixturePack(source);
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-uninstall-residual-ws-'));
    await installPack(workspace, { type: 'local', path: source });
    const grammarPath = fieldGrammarPath(workspace, 'git', 'ref');
    const before = await loadFieldGrammar(workspace, 'git', 'ref');
    const next = mergeFieldGrammar(before, inferFieldGrammar('extra/local-observation'));
    await writeFile(grammarPath, JSON.stringify(next), 'utf8');
    await uninstallPack(workspace, 'git-cli');
    const after = await loadFieldGrammar(workspace, 'git', 'ref');
    expect(after?.observations).toBeGreaterThanOrEqual(1);
  });

  it('handles uninstall when no residual grammar remains on disk', async () => {
    const source = await mkdtemp(path.join(os.tmpdir(), 'utk-uninstall-empty-src-'));
    await writeFixturePack(source);
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-uninstall-empty-ws-'));
    await installPack(workspace, { type: 'local', path: source });
    await rm(fieldGrammarPath(workspace, 'git', 'ref'), { force: true });
    await uninstallPack(workspace, 'git-cli');
    const list = await listInstalledPacks(workspace);
    expect(list).toEqual([]);
  });

  it('removes lark artifact even when seed observations are zero', async () => {
    const source = await mkdtemp(path.join(os.tmpdir(), 'utk-uninstall-nogrammar-src-'));
    await writeFixturePack(source, {
      manifest: [
        '[pack]',
        'name = "no-seed"',
        'version = "1.0.0"',
        '',
        '[[grammars]]',
        'tool = "git"',
        'field = "ref"',
        ''
      ].join('\n'),
      tools: {},
      grammars: { 'grammars/git/ref.lark': defaultLark() },
      templates: {}
    });
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-uninstall-nogrammar-ws-'));
    await installPack(workspace, { type: 'local', path: source });
    await uninstallPack(workspace, 'no-seed');
    await expect(stat(path.join(workspace, '.utk', 'tools', 'git', 'fields', 'ref.lark'))).rejects.toThrow();
  });

  it('caches template descriptors when templates are present', async () => {
    const source = await mkdtemp(path.join(os.tmpdir(), 'utk-install-tpl-src-'));
    await writeFixturePack(source);
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-install-tpl-ws-'));
    await installPack(workspace, { type: 'local', path: source });
    const cachePath = path.join(workspace, '.utk', 'cache', 'templates', 'git-cli.json');
    const cached = JSON.parse(await readFile(cachePath, 'utf8')) as { templates: Array<{ id: string }> };
    expect(cached.templates[0]?.id).toBe('git.checkout');
  });

  it('accepts an injected fetcher to install from non-local sources', async () => {
    const stage = await mkdtemp(path.join(os.tmpdir(), 'utk-fake-fetcher-src-'));
    await writeFixturePack(stage);
    const workspace = await mkdtemp(path.join(os.tmpdir(), 'utk-fake-fetcher-ws-'));
    const fakeFetcher: PackFetcher = async () => ({ dir: stage, revision: 'sha:deadbeef' });
    const installed = await installPack(workspace, { type: 'git', url: 'https://example.com/git-cli' }, { fetcher: fakeFetcher });
    expect(installed.revision).toBe('sha:deadbeef');
    expect(installed.source).toContain('git:https://example.com/git-cli');
  });
});

